/**
 * Representa una moneda de 100 unidades.
 * Extiende la clase base Moneda.
 */
public class Moneda100 extends Moneda {

    /**
     * Constructor por defecto para la moneda de 100 unidades.
     * Llama al constructor de la clase base Moneda.
     */
    public Moneda100() {
        super();
    }

    /**
     * Obtiene el valor de la moneda (100 unidades).
     *
     * @return El valor de la moneda (100).
     */
    public int getValor() {
        return 100;
    }
}
